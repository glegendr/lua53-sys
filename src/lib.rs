#[inline(always)]
#[allow(non_snake_case)]
pub unsafe fn luaL_loadbuffer(
    L: *mut lua_State,
    s: *const c_char,
    sz: usize,
    n: *const c_char,
) -> c_int {
    unsafe { luaL_loadbufferx(L, s, sz, n, ptr::null()) }
}

#[inline(always)]
#[allow(non_snake_case)]
pub unsafe fn luaL_loadbufferenv(
    L: *mut lua_State,
    data: *const c_char,
    size: usize,
    name: *const c_char,
    mode: *const c_char,
    mut env: c_int,
) -> c_int {
    unsafe {
        if env != 0 {
            env = lua_absindex(L, env);
        }
        let status = luaL_loadbufferx(L, data, size, name, mode);
        if status == LUA_OK && env != 0 {
            lua_pushvalue(L, env);
            lua_setupvalue(L, -2, 1);
        }
        status
    }
}
