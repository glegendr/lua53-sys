#[allow(
    dead_code,
    non_camel_case_types,
    non_snake_case,
    non_upper_case_globals
)]
mod bindings {
    include!(concat!(env!("OUT_DIR"), "/bindings.rs"));

    // mlua compability for Option<fn(...)> callbacks
    pub type lua_CFunction = unsafe extern "C-unwind" fn(L: *mut lua_State) -> ::core::ffi::c_int;
    pub type lua_Alloc = unsafe extern "C" fn(
        ud: *mut ::core::ffi::c_void,
        ptr: *mut ::core::ffi::c_void,
        osize: usize,
        nsize: usize,
    ) -> *mut ::core::ffi::c_void;
    pub type lua_Hook = unsafe extern "C-unwind" fn(L: *mut lua_State, ar: *mut lua_Debug);

    // redefine lua_Debug to avoid bindgen making it opaque
    #[repr(C)]
    pub struct lua_Debug {
        pub event: ::core::ffi::c_int,
        pub name: *const ::core::ffi::c_char,
        pub namewhat: *const ::core::ffi::c_char,
        pub what: *const ::core::ffi::c_char,
        pub source: *const ::core::ffi::c_char,
        pub currentline: ::core::ffi::c_int,
        pub linedefined: ::core::ffi::c_int,
        pub lastlinedefined: ::core::ffi::c_int,
        pub nups: ::core::ffi::c_uchar,
        pub nparams: ::core::ffi::c_uchar,
        pub isvararg: ::core::ffi::c_char,
        pub istailcall: ::core::ffi::c_char,
        pub short_src: [::core::ffi::c_char; 60usize],
        pub i_ci: *mut ::core::ffi::c_void,
    }

    // lua_Writer is blocklisted so we can declare it without Option (non-nullable in mlua usage)
    pub type lua_Writer = unsafe extern "C-unwind" fn(
        L: *mut lua_State,
        p: *const ::core::ffi::c_void,
        sz: usize,
        ud: *mut ::core::ffi::c_void,
    ) -> ::core::ffi::c_int;
    pub type lua_KFunction = ::core::option::Option<
        unsafe extern "C-unwind" fn(
            L: *mut lua_State,
            status: ::core::ffi::c_int,
            ctx: lua_KContext,
        ) -> ::core::ffi::c_int,
    >;
}
